package net.plateau.common.util.readInfo.json;

public class Dirs {
    private String gameDir;
    private String isOpenVersionIsolation;
    private String JavaDir;

    // Getter and Setter for gameDir
    public String getGameDir() {
        return gameDir;
    }

    public void setGameDir(String gameDir) {
        this.gameDir = gameDir;
    }

    // Getter and Setter for isOpenVersionIsolation
    public String getIsOpenVersionIsolation() {
        return isOpenVersionIsolation;
    }

    public void setIsOpenVersionIsolation(String isOpenVersionIsolation) {
        this.isOpenVersionIsolation = isOpenVersionIsolation;
    }

    // Getter and Setter for JavaDir
    public String getJavaDir() {
        return JavaDir;
    }

    public void setJavaDir(String javaDir) {
        JavaDir = javaDir;
    }
}
